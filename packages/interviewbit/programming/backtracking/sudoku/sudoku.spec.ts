/**
 * Test specs for sudoku problem
 * Interviewbit - Sudoku
 */

import { convertBoardToNumber, sudoku } from './sudoku';

describe('Interviewbit - Sudoku', () => {
  describe('convertBoardToNumber', () => {
    it('should convert correctly', () => {
      expect(
        convertBoardToNumber([
          '53..7....',
          '6..195...',
          '.98....6.',
          '8...6...3',
          '4..8.3..1',
          '7...2...6',
          '.6....28.',
          '...419..5',
          '....8..79'
        ])
      ).toEqual([
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9]
      ]);
    });
  });

  describe('sudoku', () => {
    describe('Example tests', () => {
      it('should return for Example 1 test', () => {
        expect(
          sudoku([
            '53..7....',
            '6..195...',
            '.98....6.',
            '8...6...3',
            '4..8.3..1',
            '7...2...6',
            '.6....28.',
            '...419..5',
            '....8..79'
          ])
        ).toEqual([
          '534678912',
          '672195348',
          '198342567',
          '859761423',
          '426853791',
          '713924856',
          '961537284',
          '287419635',
          '345286179'
        ]);
      });
    });

    describe('Correctness tests', () => {
      it('should return for Sample 1 input', () => {
        expect(
          sudoku([
            '..9748...',
            '7........',
            '.2.1.9...',
            '..7...24.',
            '.64.1.59.',
            '.98...3..',
            '...8.3.2.',
            '........6',
            '...2759..'
          ])
        ).toEqual([
          '519748632',
          '783652419',
          '426139875',
          '357986241',
          '264317598',
          '198524367',
          '975863124',
          '832491756',
          '641275983'
        ]);
      });
    });

    describe('Performance tests', () => {
      it('should run for Medium 1 input', () => {
        sudoku([
          '..13..7.9',
          '..8......',
          '4.....2..1',
          '...5.....',
          '..3749...',
          '..2.1....',
          '.8..9..7.',
          '5...23..4',
          '..6.8..2.'
        ]);
      });
    });
  });
});
